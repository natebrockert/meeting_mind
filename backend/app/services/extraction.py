def _template_for_meeting(config: AppConfig, meeting_id: int) -> str:
    with connect(config.paths.database_path) as conn:
        row = conn.execute(
            "SELECT template FROM meetings WHERE id = ?", (meeting_id,)
        ).fetchone()
    template = (row["template"] if row and row["template"] else "general")
    return template if template in TEMPLATE_PROMPTS else "general"


def _meeting_date_iso(config: AppConfig, meeting_id: int) -> str | None:
    """Return the meeting's date as YYYY-MM-DD for relative-date resolution.

    Reads `meetings.created_at` (SQLite CURRENT_TIMESTAMP, e.g.
    '2026-05-15 18:34:21' or an ISO 'YYYY-MM-DDTHH:MM:SS' from Python).
    Returns None if the row is missing or unparseable — caller proceeds
    without the date hint rather than failing extraction.
    """
    with connect(config.paths.database_path) as conn:
        row = conn.execute(
            "SELECT created_at FROM meetings WHERE id = ?", (meeting_id,)
        ).fetchone()
    if not row or not row["created_at"]:
        return None
    raw = str(row["created_at"]).strip()
    if len(raw) < 10:
        return None
    candidate = raw[:10]
    if re.match(r"^\d{4}-\d{2}-\d{2}$", candidate):
        return candidate
    return None


def _precompute_downstream_caches(config: AppConfig, meeting_id: int) -> None:
    """Warm the per-meeting LLM caches that the dashboard would otherwise
    hit cold on first open.

    Fires drivers (which triggers enrichment internally), key_terms, and
    reflections concurrently against the worktree's ThreadPoolExecutor.
    Each callee writes its own meeting_*_cache row on success; failures
    are logged and swallowed so a partial precompute doesn't block the
    extract step from completing.

    Reflections only runs when an owner is configured AND skip_reflections
    isn't set on the meeting — otherwise the underlying service short-
    circuits before any LLM call, so the precompute cost is zero.
    """
    from concurrent.futures import ThreadPoolExecutor

    from app.services.conversation_drivers import compute_drivers_and_cog
    from app.services.reflections import compute_reflections
    from app.services.synthesis import build_synthesis_snapshot

    def _safe(name: str, fn):
        try:
            fn()
        except Exception as exc:  # noqa: BLE001 — precompute is best-effort
            LOGGER.warning(
                "precompute_failed meeting_id=%s task=%s err=%s",
                meeting_id, name, exc,
            )

    with ThreadPoolExecutor(max_workers=3) as executor:
        executor.submit(_safe, "drivers", lambda: compute_drivers_and_cog(config, meeting_id))
        executor.submit(_safe, "key_terms", lambda: build_synthesis_snapshot(config, meeting_id))
        executor.submit(_safe, "reflections", lambda: compute_reflections(config, meeting_id))


def extract_meeting_atoms(config: AppConfig, meeting_id: int) -> MeetingAtoms:
    from app.services.driver_enrichment import invalidate_driver_enrichment_cache
    from app.services.llm_drivers import invalidate_llm_drivers_cache
    from app.services.pipeline import _emit_progress
    from app.services.reflections import invalidate_reflections_cache
    from app.services.synthesis import invalidate_key_terms_cache

    _emit_progress(config, meeting_id, "extract", "running", 0.0)
    # Re-extracting changes the source content the cached LLM passes
    # operated over; drop all dependent caches so the next dashboard
    # load recomputes against the fresh transcript / atoms.
    invalidate_key_terms_cache(config, meeting_id)
    invalidate_llm_drivers_cache(config, meeting_id)
    invalidate_driver_enrichment_cache(config, meeting_id)
    invalidate_reflections_cache(config, meeting_id)
    chunks = chunk_transcript(config, meeting_id)
    if not chunks:
        _emit_progress(config, meeting_id, "extract", "complete", 1.0)
        return MeetingAtoms(
            suggested_title="Untitled Meeting",
            tldr="No transcript available.",
            summary="No transcript available.",
        )

    model_bus = ModelBus(config)
    partials: list[MeetingAtoms] = []
    schema = MeetingAtoms.model_json_schema()
    template = _template_for_meeting(config, meeting_id)
    # Append a "Meeting Context" block so the model can resolve relative
    # due dates ("by Friday") to absolute YYYY-MM-DD on the model side
    # rather than us doing brittle string parsing on the backend. Skipped
    # silently when created_at is missing or unparseable.
    meeting_date = _meeting_date_iso(config, meeting_id)
    system_prompt = TEMPLATE_PROMPTS[template]
    if meeting_date:
        system_prompt = (
            f"{system_prompt}\n\nMeeting Context: this meeting took place "
            f"on {meeting_date}. Resolve relative date phrases against "
            "this date."
        )
    total = max(1, len(chunks))
    # v0.2.6: thread quality hints from the v0.2.2 overlap detector and
    # v0.2.4 speaker reattributer into each chunk's user message. The
    # synthesis LLM is told to hedge attribution on flagged segments
    # rather than confidently quoting a speaker whose label is suspect.
    from app.services.repair.quality_hints import augmented_chunk_text

    for index, chunk in enumerate(chunks, start=1):
        augmented_user_text = augmented_chunk_text(
            config, meeting_id, chunk.text, chunk.segment_ids
        )
        payload = model_bus.chat_json(
            [
                ChatMessage("system", system_prompt),
                ChatMessage("user", augmented_user_text),
            ],
            {"name": "MeetingAtoms", "schema": schema},
        )
        partials.append(MeetingAtoms.model_validate(payload))
        # Stop short of 0.85 so the consolidation + persist tail has room.
        _emit_progress(
            config, meeting_id,
            "extract",
            "running",
            0.05 + 0.75 * (index / total),
        )

    # Use the first non-empty tldr — the opening chunk best captures "what
    # this meeting is about" since context flows forward. Per-chunk tldrs
    # from later chunks would describe sub-segments, not the whole.
    chunk_tldr = next((part.tldr.strip() for part in partials if part.tldr.strip()), "")
    # For `briefing`, take the LAST chunk that produced a complete
    # 3-sentence briefing. The LANDING sentence (briefing[2]) requires
    # the model to see how the meeting ended — first-chunk bias would
    # report an agenda-setting "no commitments" even when the meeting
    # later landed a decision. PURPOSE drift from late chunks is
    # acceptable; LANDING accuracy matters more. Field validator on
    # MeetingAtoms.briefing already enforces "exactly 3 non-empty
    # strings or empty list" so the partials we consider are already
    # well-formed.
    chunk_briefing: list[str] = next(
        (
            list(part.briefing)
            for part in reversed(partials)
            if len(part.briefing) == 3
        ),
        [],
    )
    merged = MeetingAtoms(
        suggested_title=partials[0].suggested_title,
        tldr=chunk_tldr,
        summary="\n".join(part.summary for part in partials if part.summary),
        actions=[action for part in partials for action in part.actions],
        decisions=[decision for part in partials for decision in part.decisions],
        workstreams=[workstream for part in partials for workstream in part.workstreams],
        participant_contributions=_merge_participant_contributions(partials),
        chapter_markers=_merge_chapter_markers(partials),
        themes=_merge_themes(partials),
        stat_callouts=_merge_stat_callouts(partials),
        tension_points=_merge_tension_points(partials),
        open_questions=_dedupe_open_questions(
            [question for part in partials for question in part.open_questions]
        ),
        uncertainties=[uncertainty for part in partials for uncertainty in part.uncertainties],
        key_takeaways=_merge_key_takeaways(partials),
        briefing=chunk_briefing,
    )
    # When a long transcript produced overlapping workstreams across chunks,
    # ask the model to consolidate semantic duplicates (e.g. "Marketing
    # Strategy" + "Strategy Marketing") into a deduplicated, ranked list.
    # Workstream consolidation: tighter trigger (>3 instead of >4) so
    # meetings with 4-5 near-duplicate workstreams (e.g. three labels
    # that all describe the same strategic thread) get consolidated
    # too. Long transcripts that yield 7+ workstreams almost always
    # have heavy overlap; the prior >4 threshold under-deduped them.
    if len(merged.workstreams) > 3:
        _emit_progress(config, meeting_id, "extract", "running", 0.88)
        merged.workstreams = _consolidate_workstreams(model_bus, merged.workstreams)
    # Synthesize the executive recap before persisting so persist_atoms
    # can store the recap alongside the atoms in a single transaction.
    # Failures here are silent: the dashboard falls back to the existing
    # summary field if the recap is missing.
    _emit_progress(config, meeting_id, "extract", "running", 0.92)
    merged.executive_recap = synthesize_executive_recap(model_bus, merged)
    _emit_progress(config, meeting_id, "extract", "running", 0.94)
    persist_atoms(config, meeting_id, merged)
    # Precompute downstream caches in parallel so the first dashboard
    # open after extract is instant. Each of these would otherwise fire
    # on the GET /api/meetings/{id} cold path (drivers ~60s, enrichment
    # ~30s, key_terms ~30s, reflections ~30s on OpenRouter). Running
    # them now — while the user is already waiting on `mm extract` —
    # moves the latency to a moment the user expects to wait, and the
    # ThreadPoolExecutor halves wall-clock by running independent calls
    # concurrently. Failures here are non-fatal: they log a warning and
    # the dashboard's cache-miss path picks them up later.
    _emit_progress(config, meeting_id, "extract", "running", 0.96)
    _precompute_downstream_caches(config, meeting_id)
    # Auto-send to Obsidian if the user opted in AND speaker review is
    # already complete (every distinct diarization speaker has a
    # user-confirmed assignment). Only fires when the vault dir exists;
    # otherwise the silent no-op would mask the missing-Obsidian
    # condition.
    if config.dashboard.auto_send_to_obsidian and config.paths.vault_dir.exists():
        try:
            with connect(config.paths.database_path) as _conn:
                total = _conn.execute(
                    "SELECT COUNT(DISTINCT diarization_speaker_id) "
                    "FROM transcript_segments WHERE meeting_id = ?",
                    (meeting_id,),
                ).fetchone()[0]
                confirmed = _conn.execute(
                    "SELECT COUNT(*) FROM speaker_assignments "
                    "WHERE meeting_id = ? AND confirmed_by_user = 1",
                    (meeting_id,),
                ).fetchone()[0]
            speakers_done = total > 0 and confirmed >= total
            if speakers_done:
                from app.services.obsidian_writer import promote_meeting

                promote_meeting(config, meeting_id)
                LOGGER.info("auto_promoted meeting_id=%s", meeting_id)
        except Exception as exc:  # noqa: BLE001 — auto-promote must not block extract
            LOGGER.warning("auto_promote_failed meeting_id=%s err=%s", meeting_id, exc)
    _emit_progress(config, meeting_id, "extract", "complete", 1.0)
    return merged


# Action-clustering token-stripping set. Stopwords proper plus action-verb
# noise that drowns out the real signal in short task strings: "send the
# deck" and "share the deck" should cluster on `deck`, not be pulled apart
# by "send" vs "share".
_ACTION_STOP_TOKENS = frozenset({
    "the", "a", "an", "is", "are", "do", "does", "did", "will", "would",
    "could", "should", "in", "on", "at", "to", "for", "of", "and", "or",
    "we", "you", "this", "that", "be", "by", "with", "from", "into", "onto",
    "send", "share", "give", "make", "get", "ill", "im", "have", "has", "had",
    "let", "lets", "want", "need", "going", "gonna", "team", "her",
    "him", "his", "their", "them", "they", "out", "up", "after", "before",
})


def _action_tokens(text: str) -> set[str]:
    return {
        w
        for w in re.findall(r"[a-z0-9]+", text.lower())
        if len(w) > 2 and w not in _ACTION_STOP_TOKENS
    }


def _cluster_actions(
    actions: list[ExtractedAction],
    owner_ids: list[int | None],
) -> list[list[int]]:
    """Group near-duplicate actions into clusters.

    Returns a list of clusters, each a list of indices into `actions` in
    original order. Standalone actions appear as single-item lists. The
    first index in each cluster is the canonical (most specific phrasing).

    Heuristic — token Jaccard ≥ 0.55 AND (same owner OR ≥1 shared source
    segment). The owner-or-segment gate stops over-merging across
    complementary commitments by different speakers ("you connect us" /
    "I'll share notes" share the segment but different owners → keep
    apart; "send the deck" / "circulate the slides" share the owner →
    merge).
    """
    n = len(actions)
    if n <= 1:
        return [[i] for i in range(n)]

    tokens = [_action_tokens(a.task) for a in actions]
    seg_sets = [set(a.source_segment_ids) for a in actions]
    cluster_of: list[int] = list(range(n))  # each starts in its own cluster

    for i in range(n):
        if not tokens[i]:
            continue
        for j in range(i):
            if cluster_of[j] != j:
                continue  # only compare against existing cluster heads
            if not tokens[j]:
                continue
            inter = len(tokens[i] & tokens[j])
            union = len(tokens[i] | tokens[j])
            if union == 0:
                continue
            jaccard = inter / union
            if jaccard < 0.55:
                continue
            same_owner = (
                owner_ids[i] is not None
                and owner_ids[j] is not None
                and owner_ids[i] == owner_ids[j]
            )
            shared_seg = bool(seg_sets[i] & seg_sets[j])
            if same_owner or shared_seg:
                cluster_of[i] = cluster_of[j]
                break

    groups: dict[int, list[int]] = {}
    for idx, head in enumerate(cluster_of):
        groups.setdefault(head, []).append(idx)

    # Canonical = highest priority, then longest text, then earliest
    # segment. The canonical is what the UI shows by default; members
    # back it with evidence.
    priority_rank = {"high": 0, "normal": 1, "low": 2}

    def _canonical_key(idx: int) -> tuple[int, int, int]:
        a = actions[idx]
        longest_neg = -len(a.task or "")
        earliest = min(a.source_segment_ids) if a.source_segment_ids else 10**9
        return (priority_rank.get(a.priority, 1), longest_neg, earliest)

    out: list[list[int]] = []
    for head in sorted(groups):
        members = groups[head]
        if len(members) > 1:
            members.sort(key=_canonical_key)
        out.append(members)
    return out


def _resolve_cluster_due_date(
    members: list[ExtractedAction],
) -> tuple[str | None, int | None, list[dict[str, object]]]:
    """Pick the cluster's due date from member commitments.

    Rule: among members with a non-null `due_date`, the one mentioned in
    the latest source segment wins. Undated paraphrases don't overwrite a
    real earlier commitment. If two members carry different non-null
    dates, the chronologically later mention wins but `history` records
    the prior commitments so the UI can show "Tue (was Fri earlier)".
    """
    dated = [
        (
            max(a.source_segment_ids) if a.source_segment_ids else -1,
            a.due_date,
            a.due_date_source,
        )
        for a in members
        if a.due_date
    ]
    if not dated:
        return None, None, []
    # Sort by latest segment first, breaking ties on the date string
    # itself so the result is fully deterministic when two members share
    # the same `max(source_segment_ids)`. ISO-8601 dates sort
    # chronologically as plain strings, so the later calendar date wins
    # on a same-segment tie.
    dated.sort(key=lambda t: (t[0], t[1] or ""))
    latest = dated[-1]
    history = [
        {"date": d, "segment_id": seg if seg > -1 else None}
        for seg, d, _ in dated[:-1]
        if d != latest[1]
    ]
    return latest[1], latest[2], history


def _dedupe_open_questions(
    questions: list[ExtractedOpenQuestion],
) -> list[ExtractedOpenQuestion]:
    """Cheap semantic dedup on open questions.

    Frontier and local models alike emit near-duplicate questions when a
    transcript repeats a topic across chunks. Example seen in the wild:
    "Can the team build a referral pool of 20 patients per month within
    three months?" and "Can the team build a referral pool of 20
    patients per month within three months in Minneapolis?" — same
    question with slightly different scope. We keep the longer / more
    specific phrasing and merge source_segment_ids.

    Heuristic (no LLM call): token-set Jaccard similarity on the
    lowercase tokens, ignoring stopwords. Pairs above the threshold
    are considered duplicates and merged into the longer one. Order
    is preserved (first occurrence wins for the "earliest segment"
    sort key downstream).
    """
    if len(questions) <= 1:
        return list(questions)

    def tokens(text: str) -> set[str]:
        stop = {
            "the", "a", "an", "is", "are", "do", "does", "did", "will",
            "would", "could", "should", "in", "on", "at", "to", "for",
            "of", "and", "or", "we", "you", "this", "that", "be", "by",
        }
        words = re.findall(r"[a-z0-9]+", text.lower())
        return {w for w in words if len(w) > 2 and w not in stop}

    kept: list[ExtractedOpenQuestion] = []
    kept_tokens: list[set[str]] = []
    for q in questions:
        t = tokens(q.question)
        if not t:
            kept.append(q)
            kept_tokens.append(t)
            continue
        merged = False
        for i, kt in enumerate(kept_tokens):
            if not kt:
                continue
            inter = len(t & kt)
            union = len(t | kt)
            jaccard = inter / union if union else 0
            # 0.75 threshold catches the Minneapolis duplicate (token
            # sets overlap heavily) without over-merging genuinely
            # distinct questions (which typically share <0.4 of tokens).
            if jaccard >= 0.75:
                # Prefer the longer / more specific phrasing; merge
                # source_segment_ids so evidence pills carry both
                # citations.
                if len(q.question) > len(kept[i].question):
                    seg_ids = list(
                        dict.fromkeys(
                            kept[i].source_segment_ids + q.source_segment_ids
                        )
                    )
                    kept[i] = q.model_copy(update={"source_segment_ids": seg_ids})
                    kept_tokens[i] = t
                else:
                    seg_ids = list(
                        dict.fromkeys(
                            kept[i].source_segment_ids + q.source_segment_ids
                        )
                    )
                    kept[i] = kept[i].model_copy(update={"source_segment_ids": seg_ids})
                merged = True
                break
        if not merged:
            kept.append(q)
            kept_tokens.append(t)
    return kept


def _merge_key_takeaways(partials: list[MeetingAtoms]) -> list[str]:
    """De-duplicate key_takeaways across chunks (case-insensitive
    prefix match — chunks tend to repeat the same takeaway with minor
    wording differences). Cap at 5; the first non-empty wording wins.
    """
    seen: list[str] = []
    out: list[str] = []
    for part in partials:
        for raw in part.key_takeaways:
            clean = (raw or "").strip().rstrip(".") + "."
            if not clean or clean == ".":
                continue
            head_full = clean.casefold()
            head_key = head_full[:30]
            # Match on shared 30-char opening (or full string when shorter)
            # so two takeaways that differ only in suffix dedupe correctly.
            # Bug-fix: previously compared seen-entries' .startswith(head[:30])
            # which fails for stored entries shorter than 30 chars.
            if any(s[:30] == head_key for s in seen):
                continue
            seen.append(head_full)
            out.append(clean)
            if len(out) >= 5:
                return out
    return out


def _merge_themes(partials: list[MeetingAtoms]) -> list[str]:
    """Dedupe theme strings across chunks (case-insensitive), cap at 3.

    Themes are the same across the meeting — chunks usually repeat them.
    """
    seen: set[str] = set()
    out: list[str] = []
    for part in partials:
        for raw in part.themes:
            clean = (raw or "").strip()
            if not clean or clean.casefold() in seen:
                continue
            seen.add(clean.casefold())
            out.append(clean)
            if len(out) >= 3:
                return out
    return out


def _merge_stat_callouts(partials: list[MeetingAtoms]) -> list[StatCallout]:
    """De-duplicate stat callouts by label (case-insensitive). Keep at
    most 3. Earlier chunks win — they tend to capture the framing
    statistics; later chunks repeat or add tactical numbers.
    """
    seen: set[str] = set()
    out: list[StatCallout] = []
    for part in partials:
        for entry in part.stat_callouts:
            label = (entry.label or "").strip()
            value = (entry.value or "").strip()
            if not label or not value:
                continue
            key = label.casefold()
            if key in seen:
                continue
            seen.add(key)
            out.append(
                StatCallout(
                    value=value[:60],
                    label=label[:80],
                    source_segment_ids=list(entry.source_segment_ids),
                )
            )
            if len(out) >= 3:
                return out
    return out


def _merge_tension_points(partials: list[MeetingAtoms]) -> list[TensionPoint]:
    """De-duplicate tension points by title (case-insensitive). Keep at
    most 2; tension is rare and most meetings produce 0-1.
    """
    seen: set[str] = set()
    out: list[TensionPoint] = []
    for part in partials:
        for entry in part.tension_points:
            title = (entry.title or "").strip()
            pos = (entry.positive_side or "").strip()
            neg = (entry.negative_side or "").strip()
            if not title or not (pos or neg):
                continue
            key = title.casefold()
            if key in seen:
                continue
            seen.add(key)
            out.append(
                TensionPoint(
                    title=title[:120],
                    positive_side=pos[:280],
                    negative_side=neg[:280],
                    source_segment_ids=list(entry.source_segment_ids),
                )
            )
            if len(out) >= 2:
                return out
    return out


def _merge_chapter_markers(partials: list[MeetingAtoms]) -> list[ChapterMarker]:
    """Combine per-chunk chapter lists into a single chronological set.

    Sort by start_segment_id, drop duplicates (within 4 segment-ids of a
    previous one), cap label at 7 words, and limit to 12 chapters total
    so the rail stays scannable on long meetings.
    """
    raw = [chapter for part in partials for chapter in part.chapter_markers]
    raw.sort(key=lambda chapter: chapter.start_segment_id)
    out: list[ChapterMarker] = []
    for chapter in raw:
        label = " ".join((chapter.label or "").strip().split())
        words = label.split(" ")
        if len(words) > 7:
            label = " ".join(words[:7])
        if not label:
            continue
        if out and abs(chapter.start_segment_id - out[-1].start_segment_id) < 4:
            continue
        out.append(ChapterMarker(label=label, start_segment_id=chapter.start_segment_id))
        if len(out) >= 12:
            break
    return out


def _merge_participant_contributions(
    partials: list[MeetingAtoms],
) -> list[ParticipantContribution]:
    """Combine per-chunk contribution lists into one per-speaker entry.

    A given speaker may appear in multiple chunks; we concatenate their
    contribution sentences with " · " separators and de-duplicate by
    case-insensitive speaker name. Source segment ids are pooled.
    """
    by_speaker: dict[str, dict] = {}
    for part in partials:
        for entry in part.participant_contributions:
            name = entry.speaker.strip()
            if not name or not entry.contribution.strip():
                continue
            key = name.casefold()
            bucket = by_speaker.setdefault(
                key,
                {"speaker": name, "lines": [], "segments": []},
            )
            text = entry.contribution.strip()
            # Avoid identical-string duplication when later chunks repeat
            # the same observation about a speaker.
            if not any(line.casefold() == text.casefold() for line in bucket["lines"]):
                bucket["lines"].append(text)
            bucket["segments"].extend(entry.source_segment_ids)
    return [
        ParticipantContribution(
            speaker=data["speaker"],
            contribution=" · ".join(data["lines"]),
            source_segment_ids=list(dict.fromkeys(data["segments"])),
        )
        for data in by_speaker.values()
    ]


def _coerce_int_id(value) -> int | None:
    """Accept either int or integer-valued float / numeric string ids."""
    try:
        if isinstance(value, bool):  # bool is a subclass of int in Python
            return None
        if isinstance(value, int):
            return value
        if isinstance(value, float) and value.is_integer():
            return int(value)
        if isinstance(value, str) and value.strip().lstrip("-").isdigit():
            return int(value)
    except (TypeError, ValueError):
        return None
    return None


