# Match "Speaker 2", "Speaker_002", "Speaker 12" anywhere in prose. The
# expanded patterns cover the two diarization conventions we see in the
# wild plus the model's occasional "Speaker 4" hallucination invented
# out of confirmed names. Replacement is conservative — `a team member`
# instead of guessing — because misattribution is worse than vagueness.
_SPEAKER_PLACEHOLDER_RE = re.compile(
    r"\bSpeaker[_ ]?\d{1,3}\b",
    flags=re.IGNORECASE,
)


# First-person commitment markers — used by persist_atoms when the model
# emits an action task as a direct quote ("I'll write up the scope cut")
# without filling in `owner`. "We will" / "let's" / "we should" are GROUP
# commitments and intentionally excluded — they shouldn't credit a single
# speaker. Compiled at module level so we don't pay the cost per-call.
_FIRST_PERSON_COMMIT = re.compile(
    r"^\s*(?:i (?:will|'ll|am going to|need to|plan to)|i'll|let me|"
    r"i (?:am|'m) going to|i'?d like to)\b",
    re.IGNORECASE,
)


def _scrub_speaker_placeholders(text: str) -> str:
    """Strip 'Speaker N' / 'Speaker_002' placeholders from prose fields.

    The prompt asks the model never to emit these in user-facing prose
    (it's instructed to say 'a team member' instead), but frontier and
    local models alike occasionally regress. This belt-and-braces pass
    catches leakage at persist time so the reader never sees a placeholder.
    """
    if not text:
        return text
    return _SPEAKER_PLACEHOLDER_RE.sub("a team member", text)


def _scrub_atoms_speaker_placeholders(atoms: MeetingAtoms) -> None:
    """Mutates atoms in place — runs the placeholder scrub on every
    user-facing prose field. Centralising the list here means new
    prose fields added later get covered when their authors thread
    them through here.
    """
    atoms.tldr = _scrub_speaker_placeholders(atoms.tldr)
    atoms.summary = _scrub_speaker_placeholders(atoms.summary)
    atoms.suggested_title = _scrub_speaker_placeholders(atoms.suggested_title)
    for decision in atoms.decisions:
        decision.decision = _scrub_speaker_placeholders(decision.decision)
        if decision.rationale:
            decision.rationale = _scrub_speaker_placeholders(decision.rationale)
    for oq in atoms.open_questions:
        oq.question = _scrub_speaker_placeholders(oq.question)
    for contribution in atoms.participant_contributions:
        contribution.contribution = _scrub_speaker_placeholders(contribution.contribution)
    for chapter in atoms.chapter_markers:
        chapter.summary = _scrub_speaker_placeholders(chapter.summary)
        chapter.label = _scrub_speaker_placeholders(chapter.label)
    for tension in atoms.tension_points:
        tension.title = _scrub_speaker_placeholders(tension.title)
        tension.positive_side = _scrub_speaker_placeholders(tension.positive_side)
        tension.negative_side = _scrub_speaker_placeholders(tension.negative_side)
    for stat in atoms.stat_callouts:
        stat.value = _scrub_speaker_placeholders(stat.value)
        stat.label = _scrub_speaker_placeholders(stat.label)
    for action in atoms.actions:
        action.task = _scrub_speaker_placeholders(action.task)
        if action.owner:
            scrubbed_owner = _scrub_speaker_placeholders(action.owner)
            # If the owner string was nothing BUT a placeholder, the
            # scrub yields "a team member" — drop to None so the UI
            # renders the action as unassigned rather than as a fake
            # display name.
            action.owner = (
                None
                if scrubbed_owner.strip().casefold() == "a team member"
                else scrubbed_owner
            )
    for workstream in atoms.workstreams:
        workstream.name = _scrub_speaker_placeholders(workstream.name)
        workstream.description = _scrub_speaker_placeholders(workstream.description)
    atoms.key_takeaways = [
        _scrub_speaker_placeholders(t) for t in atoms.key_takeaways
    ]


def persist_atoms(config: AppConfig, meeting_id: int, atoms: MeetingAtoms) -> None:
    # Belt-and-braces scrub of `Speaker N` placeholders that leak into
    # prose fields despite the prompt rule. Runs once on the merged
    # atoms before persistence so every downstream surface (overview,
    # exports, Reflections) sees clean prose.
    _scrub_atoms_speaker_placeholders(atoms)
    with connect(config.paths.database_path) as conn:
        conn.execute(
            "UPDATE meetings SET title = ? WHERE id = ?",
            (atoms.suggested_title, meeting_id),
        )
        conn.execute("DELETE FROM action_items WHERE meeting_id = ?", (meeting_id,))
        conn.execute(
            """
            DELETE FROM review_items
            WHERE meeting_id = ?
              AND kind IN (
                'summary', 'decision', 'workstream', 'open_question', 'uncertainty',
                'action_cluster_meta', 'executive_recap'
              )
            """,
            (meeting_id,),
        )
        conn.execute(
            """
            INSERT INTO review_items
              (meeting_id, kind, title, payload_json, confidence, source_segment_ids)
            VALUES (?, ?, ?, ?, ?, ?)
            """,
            (
                meeting_id,
                "summary",
                atoms.suggested_title,
                json.dumps(
                    {
                        "summary": atoms.summary,
                        "tldr": atoms.tldr,
                        "participant_contributions": [
                            entry.model_dump() for entry in atoms.participant_contributions
                        ],
                        "chapter_markers": [
                            chapter.model_dump() for chapter in atoms.chapter_markers
                        ],
                        "themes": list(atoms.themes),
                        "stat_callouts": [
                            entry.model_dump() for entry in atoms.stat_callouts
                        ],
                        "tension_points": [
                            entry.model_dump() for entry in atoms.tension_points
                        ],
                        "key_takeaways": list(atoms.key_takeaways),
                        "briefing": list(atoms.briefing),
                    }
                ),
                0.8,
                "[]",
            ),
        )
        if atoms.executive_recap is not None:
            conn.execute(
                """
                INSERT INTO review_items
                  (meeting_id, kind, title, payload_json)
                VALUES (?, 'executive_recap', ?, ?)
                """,
                (
                    meeting_id,
                    atoms.suggested_title[:120],
                    atoms.executive_recap.model_dump_json(),
                ),
            )
        # Build a lookup from approved speaker labels → people.id, scoped
        # to the *confirmed speakers of this meeting* so an action whose
        # owner says "Sam" can't collide with a different "Sam" from an
        # unrelated meeting.
        owner_to_person: dict[str, int] = {}
        people_rows = conn.execute(
            """
            SELECT DISTINCT p.id, p.display_name, p.aliases
            FROM people p
            JOIN speaker_assignments sa ON sa.person_id = p.id
            WHERE sa.meeting_id = ? AND sa.confirmed_by_user = 1
            """,
            (meeting_id,),
        ).fetchall()
        for row in people_rows:
            owner_to_person[str(row["display_name"]).strip().casefold()] = int(row["id"])
            try:
                aliases = json.loads(row["aliases"] or "[]")
            except json.JSONDecodeError:
                aliases = []
            for alias in aliases:
                if isinstance(alias, str) and alias.strip():
                    owner_to_person[alias.strip().casefold()] = int(row["id"])

        # Build a (segment_id → person_id) lookup so we can infer the
        # owner of a first-person-commitment action from the speaker at
        # the segment where it was raised. Only includes segments whose
        # speakers are confirmed; unconfirmed segments don't credit
        # anyone (avoids mis-attribution).
        segment_speaker_person: dict[int, int] = {}
        for seg_row in conn.execute(
            """
            SELECT ts.id AS sid, sa.person_id
            FROM transcript_segments ts
            JOIN speaker_assignments sa
              ON sa.meeting_id = ts.meeting_id
             AND sa.diarization_speaker_id = ts.diarization_speaker_id
             AND sa.confirmed_by_user = 1
            WHERE ts.meeting_id = ?
            """,
            (meeting_id,),
        ).fetchall():
            if seg_row["person_id"] is not None:
                segment_speaker_person[int(seg_row["sid"])] = int(seg_row["person_id"])

        def _resolve_owner(action: ExtractedAction) -> int | None:
            """Walk the 3-step owner resolution (display-name → first-name
            fallback → segment speaker for first-person commitments → new
            people row when the string looks like a real name).
            """
            owner_raw = (action.owner or "").strip()
            owner_id: int | None = None
            if owner_raw:
                owner_id = owner_to_person.get(owner_raw.casefold())
                if owner_id is None:
                    token = owner_raw.split()[0].casefold().strip("@.,;:'\"")
                    candidates = {
                        person_id
                        for key, person_id in owner_to_person.items()
                        if key == token or key.split()[0] == token
                    }
                    if len(candidates) == 1:
                        owner_id = next(iter(candidates))
            if owner_id is None and _FIRST_PERSON_COMMIT.search(action.task):
                seed_seg = (
                    max(action.source_segment_ids)
                    if action.source_segment_ids
                    else None
                )
                if seed_seg is not None:
                    seg_owner = segment_speaker_person.get(int(seed_seg))
                    if seg_owner is not None:
                        owner_id = seg_owner
            if owner_id is None and owner_raw:
                looks_like_name = bool(
                    re.match(r"^[A-Z][A-Za-z'\-\.]{1,40}(\s+[A-Za-z'\-\.]{1,40}){1,3}$", owner_raw)
                )
                if looks_like_name:
                    existing = conn.execute(
                        "SELECT id FROM people WHERE lower(display_name) = ?",
                        (owner_raw.casefold(),),
                    ).fetchone()
                    if existing:
                        owner_id = int(existing["id"])
                    else:
                        cursor = conn.execute(
                            "INSERT INTO people (display_name, last_seen_at) "
                            "VALUES (?, CURRENT_TIMESTAMP)",
                            (owner_raw,),
                        )
                        owner_id = int(cursor.lastrowid)
                    owner_to_person[owner_raw.casefold()] = owner_id
            return owner_id

        owner_ids: list[int | None] = [_resolve_owner(a) for a in atoms.actions]
        clusters = _cluster_actions(atoms.actions, owner_ids)
        for cluster in clusters:
            if len(cluster) == 1:
                idx = cluster[0]
                action = atoms.actions[idx]
                conn.execute(
                    """
                    INSERT INTO action_items
                      (meeting_id, owner_person_id, text, due_date, due_date_source,
                       priority, source_segment_ids)
                    VALUES (?, ?, ?, ?, ?, ?, ?)
                    """,
                    (
                        meeting_id,
                        owner_ids[idx],
                        action.task,
                        action.due_date,
                        action.due_date_source,
                        action.priority,
                        json.dumps(action.source_segment_ids),
                    ),
                )
                continue
            # Cluster of 2+: write the canonical first, capture its id,
            # then write members linked to it. Canonical takes the merged
            # segment IDs (so evidence pills carry every member's
            # citations) and the resolved cluster-level due date.
            canon_idx = cluster[0]
            canonical = atoms.actions[canon_idx]
            member_actions = [atoms.actions[i] for i in cluster]
            merged_segs: list[int] = []
            for a in member_actions:
                for seg in a.source_segment_ids:
                    if seg not in merged_segs:
                        merged_segs.append(seg)
            due_date, due_date_source, due_history = _resolve_cluster_due_date(
                member_actions
            )
            cursor = conn.execute(
                """
                INSERT INTO action_items
                  (meeting_id, owner_person_id, text, due_date, due_date_source,
                   priority, source_segment_ids, cluster_role)
                VALUES (?, ?, ?, ?, ?, ?, ?, 'canonical')
                """,
                (
                    meeting_id,
                    owner_ids[canon_idx],
                    canonical.task,
                    due_date,
                    due_date_source,
                    canonical.priority,
                    json.dumps(merged_segs),
                ),
            )
            canonical_id = int(cursor.lastrowid)
            for member_idx in cluster[1:]:
                member = atoms.actions[member_idx]
                conn.execute(
                    """
                    INSERT INTO action_items
                      (meeting_id, owner_person_id, text, due_date, due_date_source,
                       priority, source_segment_ids, cluster_id, cluster_role)
                    VALUES (?, ?, ?, ?, ?, ?, ?, ?, 'member')
                    """,
                    (
                        meeting_id,
                        owner_ids[member_idx],
                        member.task,
                        member.due_date,
                        member.due_date_source,
                        member.priority,
                        json.dumps(member.source_segment_ids),
                        canonical_id,
                    ),
                )
            if due_history:
                # Persist the supersession trail on the canonical via a
                # dedicated review_items row keyed off the action id.
                conn.execute(
                    """
                    INSERT INTO review_items
                      (meeting_id, kind, title, payload_json)
                    VALUES (?, 'action_cluster_meta', ?, ?)
                    """,
                    (
                        meeting_id,
                        f"action:{canonical_id}",
                        json.dumps({"due_date_history": due_history}),
                    ),
                )
        for decision in atoms.decisions:
            conn.execute(
                """
                INSERT INTO review_items
                  (meeting_id, kind, title, payload_json, confidence, source_segment_ids)
                VALUES (?, ?, ?, ?, ?, ?)
                """,
                (
                    meeting_id,
                    "decision",
                    decision.decision[:120],
                    decision.model_dump_json(),
                    0.7,
                    json.dumps(decision.source_segment_ids),
                ),
            )
        for workstream in _top_workstreams(atoms.workstreams):
            conn.execute(
                """
                INSERT INTO review_items
                  (meeting_id, kind, title, payload_json, confidence, source_segment_ids)
                VALUES (?, ?, ?, ?, ?, ?)
                """,
                (
                    meeting_id,
                    "workstream",
                    workstream.name,
                    workstream.model_dump_json(),
                    workstream.confidence,
                    json.dumps(workstream.source_segment_ids),
                ),
            )
        for question in atoms.open_questions:
            conn.execute(
                """
                INSERT INTO review_items
                  (meeting_id, kind, title, payload_json, source_segment_ids)
                VALUES (?, ?, ?, ?, ?)
                """,
                (
                    meeting_id,
                    "open_question",
                    question.question[:120],
                    question.model_dump_json(),
                    json.dumps(question.source_segment_ids),
                ),
            )
        for uncertainty in atoms.uncertainties:
            conn.execute(
                """
                INSERT INTO review_items (meeting_id, kind, title, payload_json)
                VALUES (?, ?, ?, ?)
                """,
                (meeting_id, "uncertainty", uncertainty[:120], json.dumps({"text": uncertainty})),
            )


