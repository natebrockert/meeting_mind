class ExtractedAction(BaseModel):
    owner: str | None = None
    task: str
    due_date: str | None = None
    # Segment id where the due date phrase was spoken. Lets the dashboard
    # jump-to-evidence on "due by Friday" so the user can verify the model
    # parsed it correctly. Null when due_date itself is null or the source
    # is ambiguous (a date stated in passing not tied to one segment).
    due_date_source: int | None = None
    priority: str = "normal"
    source_segment_ids: list[int] = Field(default_factory=list)


class ExtractedDecision(BaseModel):
    decision: str
    # One-sentence WHY the decision was made (trade-off considered,
    # constraint cited, evidence that tipped it). Optional — legacy payloads
    # predate this field and the model is allowed to leave it null when
    # no rationale was stated. Decision logs without rationale rot fastest,
    # so the prompt pushes the model to infer from surrounding context
    # when the speaker didn't say "because" explicitly.
    rationale: str | None = None
    source_segment_ids: list[int] = Field(default_factory=list)


class ExtractedOpenQuestion(BaseModel):
    """A question raised in the meeting that wasn't fully resolved.

    Promoted from a flat string list to a structured object so the dashboard
    can show status pills, attribute questions back to the speaker who
    raised them, and feed Phase B+ Conversation Drivers / Reflections.
    Legacy persisted records used `{"text": "..."}`; readers handle both.
    """

    question: str
    # Speaker who raised the question (approved name when available, else
    # null when the transcript is ambiguous).
    raised_by: str | None = None
    # Speaker the question was directed at, when explicit ("Avery, what's
    # the rollout timeline?"). Null for broadcast questions.
    addressed_to: str | None = None
    # unanswered: no on-topic response within the next ~90s of transcript.
    # partially_answered: a response came but didn't resolve the question.
    # deferred: someone explicitly said "let's come back to this" or
    # "park it."
    status: Literal["unanswered", "partially_answered", "deferred"] = "unanswered"
    source_segment_ids: list[int] = Field(default_factory=list)


class ExtractedWorkstream(BaseModel):
    name: str
    description: str = ""
    confidence: float = 0.5
    source_segment_ids: list[int] = Field(default_factory=list)


class ParticipantContribution(BaseModel):
    """Per-attendee distillation rendered as the canonical Minutes view.

    Replaces "transcript-grouped-by-chapter" Minutes (which duplicated
    the Transcript tab) with a "what did each person bring to this
    meeting" surface.
    """

    speaker: str
    contribution: str
    source_segment_ids: list[int] = Field(default_factory=list)


class StatCallout(BaseModel):
    """Big-number highlight surfaced on the Mind Map as a stat card.

    The model picks 0-3 of the most-interesting numerical claims in the
    meeting (durations, dollar amounts, percentages, counts) and renders
    them with disproportionate visual weight in the dashboard.
    """

    value: str
    label: str
    source_segment_ids: list[int] = Field(default_factory=list)


class TensionPoint(BaseModel):
    """A moment in the meeting where two sides emerged.

    Surfaces as a sentiment-split card on the Mind Map. Most meetings
    have zero or one; the model only emits a tension when there's a
    real either/or in the transcript, not synthetic conflict.
    """

    title: str
    positive_side: str
    negative_side: str
    source_segment_ids: list[int] = Field(default_factory=list)


class ChapterMarker(BaseModel):
    """Time-anchored chapter label for the Transcript chapter rail.

    Generated by the LLM in the same extract call rather than derived
    heuristically on the frontend. Label is capped at 7 words by the
    prompt so the rail reads as labels, not paragraphs. start_segment_id
    is the segment that opens the chapter.
    """

    label: str
    start_segment_id: int
    # One-sentence skim of what happens in this chapter. Optional — legacy
    # records predate this field, and the model returns "" when a chapter
    # is too short to summarise meaningfully. Useful both for human skim
    # in the Time-Minutes view and as context for downstream passes
    # (Conversation Drivers, Reflections).
    summary: str = ""


class ExecutiveRecapBullet(BaseModel):
    """One commitment in the strategy section. `purpose` ties the
    commitment to the meeting's strategic through-line; null is allowed
    (and preferred over a fabricated rationale) when the commitment is
    tangential or the meeting had no coherent through-line.
    """

    owner: str
    commitment: str
    purpose: str | None = None


class ExecutiveRecapSection(BaseModel):
    """Header/body pair for a recap section. Both may be null only in
    the optional risk section — the reframe and strategy sections are
    always populated (fallback prompts cover degenerate cases).
    """

    header: str | None = None
    body: str | None = None


class ExecutiveRecapStrategy(BaseModel):
    header: str | None = None
    body: str | None = None
    bullets: list[ExecutiveRecapBullet] = Field(default_factory=list)
    # One-sentence aside about something deferred / parked / scoped out.
    # Optional — most meetings don't have one.
    trailer: str | None = None


class ExecutiveRecap(BaseModel):
    """Plaud-style narrative recap synthesized from MeetingAtoms.
    Three sections: reframe (editorial), strategy (commitments framed
    as one plan), risk (conservative voice, optional).
    """

    reframe: ExecutiveRecapSection
    strategy: ExecutiveRecapStrategy
    risk: ExecutiveRecapSection = Field(default_factory=ExecutiveRecapSection)


class MeetingAtoms(BaseModel):
    suggested_title: str
    # Wire-thin headline shown at the very top of the meeting page.
    # The fuller `summary` lives below it; this is the one-glance hook.
    # Required (no default) so json-schema enforcement makes the model
    # emit it instead of picking the empty-string fallback.
    tldr: str
    summary: str
    actions: list[ExtractedAction] = Field(default_factory=list)
    decisions: list[ExtractedDecision] = Field(default_factory=list)
    workstreams: list[ExtractedWorkstream] = Field(default_factory=list)
    participant_contributions: list[ParticipantContribution] = Field(default_factory=list)
    chapter_markers: list[ChapterMarker] = Field(default_factory=list)
    themes: list[str] = Field(default_factory=list)
    stat_callouts: list[StatCallout] = Field(default_factory=list)
    tension_points: list[TensionPoint] = Field(default_factory=list)
    open_questions: list[ExtractedOpenQuestion] = Field(default_factory=list)
    uncertainties: list[str] = Field(default_factory=list)
    # 3-5 model-curated takeaways that someone who wasn't in the meeting
    # would need to know. Distinct from `summary` (narrative prose) and
    # `tldr` (single hook): these are the bullets a reader would write
    # in an email after the call. Anchored to substance, not moments.
    # Optional — old payloads predate this field; renderers fall back
    # to deriving from decisions + open_questions.
    key_takeaways: list[str] = Field(default_factory=list)
    # Exactly 3 sentences that brief someone who didn't attend.
    # Distinct from `summary` (narrative prose) and `tldr` (one-glance
    # hook). Roles:
    #   [0] PURPOSE — the strategic frame / why this meeting existed
    #   [1] SUBSTANCE — what was actually discussed (concrete, not
    #       "Person A said X then B said Y" — synthesize)
    #   [2] LANDING — where it ended up: decision, open question, or
    #       next step. Honest when nothing resolved.
    # Rendered at the top of the Mind Map view. Empty when the model
    # couldn't produce a confident briefing (e.g. transcript too thin).
    briefing: list[str] = Field(default_factory=list)

    @field_validator("briefing", mode="before")
    @classmethod
    def _enforce_briefing_shape(cls, v):
        """Reject malformed briefings loudly. Only 0 (empty, valid
        fallback) or exactly 3 non-empty strings are accepted. Anything
        else — wrong length, non-string items, all-whitespace — gets
        coerced to [] with a warning so the Mind Map degrades to the
        legacy TL;DR card instead of rendering 2 sentences as if 3
        were intended.
        """
        if v in (None, [], ()):
            return []
        if not isinstance(v, list):
            logging.getLogger(__name__).warning(
                "briefing rejected: not a list (got %s)", type(v).__name__
            )
            return []
        clean = [s.strip() for s in v if isinstance(s, str) and s.strip()]
        if len(clean) != 3:
            logging.getLogger(__name__).warning(
                "briefing rejected: expected 3 non-empty strings, "
                "got %d (raw len=%d)",
                len(clean),
                len(v),
            )
            return []
        return clean
    # Second-pass narrative synthesis. None when the recap call failed
    # or the meeting had no decisions/actions/chapters to recap. The
    # field rides on atoms so persist_atoms can write it in the same
    # transaction; the LLM extraction itself never produces it (the
    # recap is a separate call against atoms-only input).
    executive_recap: ExecutiveRecap | None = None


@dataclass(frozen=True)
class TranscriptChunk:
    index: int
    text: str
    segment_ids: list[int]


def build_transcript_markdown(meeting_id: int, database_path) -> str:
    with connect(database_path) as conn:
        rows = conn.execute(
            """
            SELECT id, start_ms, end_ms, text, diarization_speaker_id
            FROM transcript_segments
            WHERE meeting_id = ?
            ORDER BY start_ms
            """,
            (meeting_id,),
        ).fetchall()
    lines = []
    for row in rows:
        timestamp = _format_timestamp(int(row["start_ms"]))
        text = safe_transcript_text(int(row["id"]), str(row["text"]))
        lines.append(
            f"- [{row['id']}] **{row['diarization_speaker_id']}** "
            f"[{timestamp}]: {text}"
        )
    return "\n".join(lines)


def chunk_transcript(config: AppConfig, meeting_id: int) -> list[TranscriptChunk]:
    with connect(config.paths.database_path) as conn:
        rows = conn.execute(
            """
            SELECT id, start_ms, text, diarization_speaker_id,
                   text_confidence, speaker_confidence, confidence
            FROM transcript_segments
            WHERE meeting_id = ?
            ORDER BY start_ms
            """,
            (meeting_id,),
        ).fetchall()
        # Resolve approved speaker labels so the LLM sees "Avery: ..." rather
        # than "Speaker 1: ...". Powers speaker-attributed summaries / actions.
        speaker_map = {
            row["diarization_speaker_id"]: row["approved_label"]
            for row in conn.execute(
                """
                SELECT diarization_speaker_id, approved_label
                FROM speaker_assignments
                WHERE meeting_id = ? AND approved_label IS NOT NULL
                """,
                (meeting_id,),
            ).fetchall()
            if row["approved_label"]
        }
        annotations = _review_annotations_by_segment(conn, meeting_id)

    chunks: list[TranscriptChunk] = []
    current: list[str] = []
    segment_ids: list[int] = []
    current_len = 0
    for row in rows:
        raw_speaker = row["diarization_speaker_id"]
        speaker = speaker_map.get(raw_speaker, raw_speaker)
        line = (
            f"[{row['id']}] {speaker} "
            f"{_format_timestamp(row['start_ms'])}: "
            f"{safe_transcript_text(int(row['id']), str(row['text']))}"
        )
        flags = _segment_extraction_flags(row, annotations.get(int(row["id"]), []))
        if flags:
            line = f"{line}\n  Review flags: {'; '.join(flags)}"
        if current and current_len + len(line) > config.chunking.target_chars:
            chunks.append(TranscriptChunk(len(chunks), "\n".join(current), list(segment_ids)))
            overlap_text = "\n".join(current)[-config.chunking.overlap_chars :]
            current = [overlap_text] if overlap_text else []
            segment_ids = segment_ids[-5:]
            current_len = len(overlap_text)
        current.append(line)
        segment_ids.append(int(row["id"]))
        current_len += len(line)
    if current:
        chunks.append(TranscriptChunk(len(chunks), "\n".join(current), list(segment_ids)))
    return chunks


