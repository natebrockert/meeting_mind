def _format_timestamp(ms: int) -> str:
    total_seconds = int(ms / 1000)
    hours, remainder = divmod(total_seconds, 3600)
    minutes, seconds = divmod(remainder, 60)
    if hours:
        return f"{hours:02}:{minutes:02}:{seconds:02}"
    return f"{minutes:02}:{seconds:02}"


def _top_workstreams(
    workstreams: list[ExtractedWorkstream],
    limit: int = 8,
) -> list[ExtractedWorkstream]:
    seen: set[str] = set()
    unique: list[ExtractedWorkstream] = []
    for workstream in sorted(workstreams, key=lambda item: item.confidence, reverse=True):
        key = workstream.name.strip().casefold()
        if not key or key in seen:
            continue
        seen.add(key)
        unique.append(workstream)
        if len(unique) == limit:
            break
    return unique


def _review_annotations_by_segment(conn, meeting_id: int) -> dict[int, list[str]]:
    rows = conn.execute(
        """
        SELECT kind, title, source_segment_ids
        FROM review_items
        WHERE meeting_id = ?
          AND status = 'open'
          AND kind IN ('transcript_quality', 'transcript_audit', 'speaker_confidence')
        """,
        (meeting_id,),
    ).fetchall()
    annotations: dict[int, list[str]] = {}
    for row in rows:
        try:
            segment_ids = json.loads(row["source_segment_ids"] or "[]")
        except json.JSONDecodeError:
            segment_ids = []
        for segment_id in segment_ids:
            if isinstance(segment_id, int):
                annotations.setdefault(segment_id, []).append(
                    f"{row['kind']}: {row['title']}"
                )
    return annotations


def _segment_extraction_flags(row, annotations: list[str]) -> list[str]:
    flags = list(annotations)
    text_confidence = (
        row["text_confidence"] if row["text_confidence"] is not None else row["confidence"]
    )
    speaker_confidence = (
        row["speaker_confidence"] if row["speaker_confidence"] is not None else row["confidence"]
    )
    if text_confidence is not None and float(text_confidence) < 0.55:
        flags.append(f"low content confidence {float(text_confidence):.2f}")
    if speaker_confidence is not None and float(speaker_confidence) < 0.7:
        flags.append(f"low speaker assignment confidence {float(speaker_confidence):.2f}")
    return flags
