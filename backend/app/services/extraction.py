def synthesize_executive_recap(
    model_bus: ModelBus, atoms: MeetingAtoms
) -> ExecutiveRecap | None:
    """Second-pass narrative synthesis from atoms.

    Reads only the structured atoms (no transcript) and produces a
    three-section Plaud-style executive recap. Returns None on any
    failure so the dashboard falls back to the existing summary
    field — synthesis is enrichment, not a hard dependency.
    """
    # Skeleton input: keep the prompt focused on what shape the model
    # needs, not on transport. Only fields the recap actually uses are
    # serialized; everything else is dropped to keep the prompt cheap.
    inputs = {
        "themes": atoms.themes,
        "decisions": [
            {
                "decision": d.decision,
                "rationale": d.rationale,
            }
            for d in atoms.decisions
        ],
        "tension_points": [
            {
                "title": t.title,
                "positive_side": t.positive_side,
                "negative_side": t.negative_side,
            }
            for t in atoms.tension_points
        ],
        "chapter_markers": [
            {
                "title": c.label,
                "summary": c.summary,
                "start_segment_id": c.start_segment_id,
            }
            for c in atoms.chapter_markers
        ],
        "actions": [
            {
                "owner": a.owner,
                "task": a.task,
                "due_date": a.due_date,
                "priority": a.priority,
            }
            for a in atoms.actions
        ],
        "key_takeaways": atoms.key_takeaways,
        "participant_contributions": [
            {
                "speaker": p.speaker,
                "summary": p.contribution,
            }
            for p in atoms.participant_contributions
        ],
    }
    if not (atoms.decisions or atoms.actions or atoms.chapter_markers):
        # Nothing substantive to recap — let the dashboard fall back.
        return None
    try:
        payload = model_bus.chat_json(
            [
                ChatMessage("system", _EXEC_RECAP_PROMPT),
                ChatMessage("user", json.dumps(inputs)),
            ],
            {"name": "ExecutiveRecap", "schema": _EXEC_RECAP_SCHEMA},
            # Editorial-voice synthesis needs the stronger model; the
            # default cheap model produces bland headers.
            model=model_bus.config.models.quality_model,
            timeout=45,
        )
        if not isinstance(payload, dict):
            return None
        return ExecutiveRecap.model_validate(payload)
    except Exception as exc:  # noqa: BLE001 — recap is best-effort
        LOGGER.warning("synthesize_executive_recap_failed err=%s", exc)
        return None


def _consolidate_workstreams(
    model_bus: ModelBus, workstreams: list[ExtractedWorkstream]
) -> list[ExtractedWorkstream]:
    """Second LLM pass to merge near-duplicate workstreams produced by per-chunk
    extraction. Falls back to the input list on any failure so the pipeline
    stays reliable.
    """
    if not workstreams:
        return workstreams
    candidates_json = json.dumps(
        [
            {
                "name": w.name,
                "description": w.description,
                "confidence": w.confidence,
                "source_segment_ids": w.source_segment_ids,
            }
            for w in workstreams
        ]
    )
    schema = {
        "type": "object",
        "properties": {
            "workstreams": {
                "type": "array",
                "items": {
                    "type": "object",
                    "properties": {
                        "name": {"type": "string"},
                        "description": {"type": "string"},
                        "confidence": {"type": "number"},
                        "source_segment_ids": {"type": "array", "items": {"type": "integer"}},
                    },
                    "required": ["name"],
                },
                "maxItems": 8,
            }
        },
        "required": ["workstreams"],
    }
    try:
        payload = model_bus.chat_json(
            [
                ChatMessage(
                    "system",
                    "You consolidate candidate meeting workstreams aggressively. "
                    "Merge any items that overlap semantically — different "
                    "wording of the same thread is the common pattern. "
                    "Example to MERGE: 'Roadmap Planning', 'Q3 Priorities', "
                    "'Launch Sequencing', 'Release Timeline' are four labels "
                    "for one thread about what ships when — collapse into a "
                    "single 'Roadmap and release sequencing' workstream. "
                    "Only merge when items refer to the SAME decisions and "
                    "actors; keep multiple workstreams when they are clearly "
                    "distinct lanes of work (e.g. 'Hiring' and 'Roadmap "
                    "Planning' are distinct lanes; 'Roadmap Planning' and "
                    "'Launch Sequencing' are the same lane). "
                    "Keep the clearest name; merge descriptions into one "
                    "concrete one-sentence summary (12-25 words) of what was "
                    "said about that thread; sum source_segment_ids; take "
                    "the max confidence. Drop generic catch-alls like "
                    "'general discussion'. Return 3 to 5 workstreams ranked "
                    "by confidence. Fewer-is-better here — better one "
                    "honest workstream than three nominally-distinct labels "
                    "for the same thing.",
                ),
                ChatMessage("user", candidates_json),
            ],
            {"name": "ConsolidatedWorkstreams", "schema": schema},
            # The quality model is more reliable at semantic merging than
            # the default; this single call decides what surfaces in the UI.
            model=model_bus.config.models.quality_model,
            # Secondary call — fail fast and keep the unconsolidated list
            # rather than hang the synthesis pass.
            timeout=45,
        )
        result = payload.get("workstreams") if isinstance(payload, dict) else None
        if isinstance(result, list) and result:
            consolidated: list[ExtractedWorkstream] = []
            for item in result:
                if not isinstance(item, dict):
                    continue
                name = str(item.get("name") or "").strip()
                if not name:
                    continue
                description = str(item.get("description") or "").strip()
                confidence_raw = item.get("confidence")
                try:
                    confidence = float(confidence_raw) if confidence_raw is not None else 0.5
                except (TypeError, ValueError):
                    confidence = 0.5
                segment_ids: list[int] = []
                for sid in item.get("source_segment_ids") or []:
                    coerced = _coerce_int_id(sid)
                    if coerced is not None:
                        segment_ids.append(coerced)
                consolidated.append(
                    ExtractedWorkstream(
                        name=name[:120],
                        description=description[:320],
                        confidence=max(0.0, min(1.0, confidence)),
                        source_segment_ids=segment_ids,
                    )
                )
            if consolidated:
                return consolidated
    except Exception as exc:  # noqa: BLE001 — best-effort consolidation
        LOGGER.warning("consolidate_workstreams_failed err=%s", exc)
        return workstreams
    return workstreams


